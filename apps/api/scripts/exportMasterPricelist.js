async function getServiceAccountAccessToken(credentialsPath) {
  const resolvedPath = resolveFromRepoRoot(credentialsPath);
  const credentials = JSON.parse(fs.readFileSync(resolvedPath, "utf8"));
  const issuedAt = Math.floor(Date.now() / 1000);
  const expiresAt = issuedAt + 60 * 60;

  const header = { alg: "RS256", typ: "JWT" };
  const claimSet = {
    iss: credentials.client_email,
    scope: "https://www.googleapis.com/auth/spreadsheets",
    aud: credentials.token_uri || "https://oauth2.googleapis.com/token",
    iat: issuedAt,
    exp: expiresAt
  };

  const unsignedToken = `${base64UrlEncode(JSON.stringify(header))}.${base64UrlEncode(JSON.stringify(claimSet))}`;
  const signature = crypto
    .createSign("RSA-SHA256")
    .update(unsignedToken)
    .sign(credentials.private_key);
  const signedJwt = `${unsignedToken}.${base64UrlEncode(signature)}`;

  const response = await fetch(credentials.token_uri, {
    method: "POST",
    headers: { "Content-Type": "application/x-www-form-urlencoded" },
    body: new URLSearchParams({
      grant_type: "urn:ietf:params:oauth:grant-type:jwt-bearer",
      assertion: signedJwt
    })
  });

  if (!response.ok) {
    throw new Error(`Google token request failed: ${response.status} ${response.statusText}`);
  }

  const payload = await response.json();
  return payload.access_token;
}

async function googleSheetRequest(url, options) {
  const response = await fetch(url, options);
  if (response.ok) {
    return response;
  }

  const text = await response.text();
  throw new Error(`Google Sheets request failed: ${response.status} ${response.statusText} ${text}`);
}

async function getGoogleSheetInfo({ accessToken, spreadsheetId, sheetName }) {
  const response = await googleSheetRequest(
    `https://sheets.googleapis.com/v4/spreadsheets/${spreadsheetId}?fields=sheets.properties`,
    {
      headers: {
        Authorization: `Bearer ${accessToken}`
      }
    }
  );

  const payload = await response.json();
  const sheets = payload.sheets || [];
  const exactMatch = sheets.find(
    (sheet) => String(sheet?.properties?.title || "") === String(sheetName)
  );
  const matchingSheet =
    exactMatch ||
    sheets.find(
      (sheet) =>
        normalizeSheetTitle(sheet?.properties?.title) === normalizeSheetTitle(sheetName)
    );

  if (!matchingSheet?.properties?.sheetId && matchingSheet?.properties?.sheetId !== 0) {
    const availableTabs = sheets
      .map((sheet) => sheet?.properties?.title)
      .filter(Boolean)
      .join(", ");
    throw new Error(
      `Google Sheet tab not found: ${sheetName}${availableTabs ? ` (available: ${availableTabs})` : ""}`
    );
  }

  return {
    sheetId: Number(matchingSheet.properties.sheetId),
    sheetName: String(matchingSheet.properties.title || sheetName)
  };
}

async function formatGoogleSheetHeader({
  accessToken,
  spreadsheetId,
  sheetName,
  columnCount
}) {
  const { sheetId } = await getGoogleSheetInfo({ accessToken, spreadsheetId, sheetName });
  const safeColumnCount = Math.max(Number(columnCount) || 1, 1);

  await googleSheetRequest(`https://sheets.googleapis.com/v4/spreadsheets/${spreadsheetId}:batchUpdate`, {
    method: "POST",
    headers: {
      Authorization: `Bearer ${accessToken}`,
      "Content-Type": "application/json"
    },
    body: JSON.stringify({
      requests: [
        {
          updateSheetProperties: {
            properties: {
              sheetId,
              gridProperties: {
                frozenRowCount: 1
              }
            },
            fields: "gridProperties.frozenRowCount"
          }
        },
        {
          repeatCell: {
            range: {
              sheetId,
              startRowIndex: 0,
              endRowIndex: 1,
              startColumnIndex: 0,
              endColumnIndex: safeColumnCount
            },
            cell: {
              userEnteredFormat: {
                textFormat: {
                  bold: true
                }
              }
            },
            fields: "userEnteredFormat.textFormat.bold"
          }
        }
      ]
    })
  });
}

async function formatGooglePricelistColumns({
  accessToken,
  spreadsheetId,
  sheetName,
  rowCount
}) {
  const { sheetId } = await getGoogleSheetInfo({ accessToken, spreadsheetId, sheetName });
  const safeRowCount = Math.max(Number(rowCount) || 1, 1);
  const requests = [];

  PRICE_SHEET_CURRENCY_COLUMNS.forEach((columnName) => {
    const index = COLUMN_INDEX_BY_NAME.get(columnName);
    if (!Number.isFinite(index)) return;
    requests.push({
      repeatCell: {
        range: {
          sheetId,
          startRowIndex: 1,
          endRowIndex: safeRowCount,
          startColumnIndex: index,
          endColumnIndex: index + 1
        },
        cell: {
          userEnteredFormat: {
            numberFormat: {
              type: "CURRENCY",
              pattern: "$#,##0.00"
            }
          }
        },
        fields: "userEnteredFormat.numberFormat"
      }
    });
  });

  PRICE_SHEET_PERCENT_COLUMNS.forEach((columnName) => {
    const index = COLUMN_INDEX_BY_NAME.get(columnName);
    if (!Number.isFinite(index)) return;
    requests.push({
      repeatCell: {
        range: {
          sheetId,
          startRowIndex: 1,
          endRowIndex: safeRowCount,
          startColumnIndex: index,
          endColumnIndex: index + 1
        },
        cell: {
          userEnteredFormat: {
            numberFormat: {
              type: "PERCENT",
              pattern: "0.00%"
            }
          }
        },
        fields: "userEnteredFormat.numberFormat"
      }
    });
  });

  PRICE_SHEET_NUMBER_COLUMNS.forEach(({ key, pattern }) => {
    const index = COLUMN_INDEX_BY_NAME.get(key);
    if (!Number.isFinite(index)) return;
    requests.push({
      repeatCell: {
        range: {
          sheetId,
          startRowIndex: 1,
          endRowIndex: safeRowCount,
          startColumnIndex: index,
          endColumnIndex: index + 1
        },
        cell: {
          userEnteredFormat: {
            numberFormat: {
              type: "NUMBER",
              pattern
            }
          }
        },
        fields: "userEnteredFormat.numberFormat"
      }
    });
  });

  if (!requests.length) return;

  await googleSheetRequest(`https://sheets.googleapis.com/v4/spreadsheets/${spreadsheetId}:batchUpdate`, {
    method: "POST",
    headers: {
      Authorization: `Bearer ${accessToken}`,
      "Content-Type": "application/json"
    },
    body: JSON.stringify({ requests })
  });
}

