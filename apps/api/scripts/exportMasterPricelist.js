function buildPackageSummary(packageRows = [], unitOfMeasure = "each") {
  return packageRows
    .map((row) => {
      const details = [];
      if (unitOfMeasure === "lbs" && toNumber(row.averageWeight) !== null && Number(row.averageWeight) > 0) {
        details.push(`${row.averageWeight} lb avg`);
      }
      if (row.quantity > 1) details.push(`${row.quantity} ea`);
      return `${row.name || `Package ${row.id}`}${details.length ? ` (${details.join(" · ")})` : ""}`;
    })
    .join(", ");
}

