async function updateGoogleSheet({ accessToken, spreadsheetId, sheetName, values }) {
  const sheetInfo = await getGoogleSheetInfo({ accessToken, spreadsheetId, sheetName });
  const resolvedSheetName = sheetInfo.sheetName;
  const encodedSheet = encodeURIComponent(resolvedSheetName);
  const columnCount = values.reduce((max, row) => Math.max(max, row?.length || 0), 1);
  const rowCount = values.length || 1;
  const endColumn = columnIndexToLetter(columnCount);
  const range = `${resolvedSheetName}!A1:${endColumn}${rowCount}`;
  const baseUrl = `https://sheets.googleapis.com/v4/spreadsheets/${spreadsheetId}/values`;

  await googleSheetRequest(`${baseUrl}/${encodedSheet}:clear`, {
    method: "POST",
    headers: {
      Authorization: `Bearer ${accessToken}`,
      "Content-Type": "application/json"
    },
    body: JSON.stringify({})
  });

  await googleSheetRequest(
    `${baseUrl}/${encodeURIComponent(range)}?valueInputOption=USER_ENTERED`,
    {
      method: "PUT",
      headers: {
        Authorization: `Bearer ${accessToken}`,
        "Content-Type": "application/json"
      },
      body: JSON.stringify({
        range,
        majorDimension: "ROWS",
        values
      })
    }
  );

  return { columnCount, rowCount, sheetName: resolvedSheetName };
}

async function syncPricelistToGoogleSheet(sheetValues, introductionValues) {
  const credentialsPath = process.env.GOOGLE_APPLICATION_CREDENTIALS;
  const spreadsheetId = process.env.GOOGLE_SHEETS_SPREADSHEET_ID;
  const sheetName = process.env.GOOGLE_SHEETS_TAB_NAME || "prices";

  if (!credentialsPath || !spreadsheetId || !sheetName) {
    throw new Error("Missing GOOGLE_APPLICATION_CREDENTIALS, GOOGLE_SHEETS_SPREADSHEET_ID, or GOOGLE_SHEETS_TAB_NAME.");
  }

  const accessToken = await getServiceAccountAccessToken(credentialsPath);
  const priceSheetUpdate = await updateGoogleSheet({
    accessToken,
    spreadsheetId,
    sheetName,
    values: sheetValues
  });
  await formatGoogleSheetHeader({
    accessToken,
    spreadsheetId,
    sheetName,
    columnCount: priceSheetUpdate.columnCount
  });
  await formatGooglePricelistColumns({
    accessToken,
    spreadsheetId,
    sheetName,
    rowCount: priceSheetUpdate.rowCount
  });
  await formatGoogleSheetHighlightedRows({
    accessToken,
    spreadsheetId,
    sheetName,
    columnCount: priceSheetUpdate.columnCount,
    rowCount: priceSheetUpdate.rowCount,
    highlightedRowIndices: sheetValues.highlightedRowIndices || []
  });
  console.log(`Google Sheet updated: ${spreadsheetId} (${sheetName})`);

  const introSheetName = process.env.GOOGLE_SHEETS_INTRO_TAB_NAME || "Introduction";
  const introSheetUpdate = await updateGoogleSheet({
    accessToken,
    spreadsheetId,
    sheetName: introSheetName,
    values: introductionValues
  });
  await formatGoogleSheetNoteRow({
    accessToken,
    spreadsheetId,
    sheetName: introSheetName,
    rowIndex: introductionValues.noteRowIndex || 0,
    columnCount: introSheetUpdate.columnCount
  });
  console.log(`Google Sheet updated: ${spreadsheetId} (${introSheetName})`);
}

async function buildSheetValues({ vendorNameMatcher = null } = {}) {
  const db = getDb();
  await ensureLocalLineSyncSchema();
  await ensureProductPricingSchema();

  const [
    productRows,
    packageRows,
    profileRows,
    saleRows,
    categoryRows,
    vendorRows
  ] = await Promise.all([
    db.select().from(products),
    db.select().from(packages),
    db.select().from(productPricingProfiles),
    db.select().from(productSales),
    db.select().from(categories),
    db.select().from(vendors)
  ]);

  let packageMetaRows = [];
  try {
    packageMetaRows = await db.select().from(localLinePackageMeta);
  } catch (error) {
    if (!isMissingTableError(error, "local_line_package_meta")) {
      throw error;
    }
  }

  const categoryMap = new Map(categoryRows.map((row) => [Number(row.id), row.name || "Uncategorized"]));
  const vendorMap = new Map(vendorRows.map((row) => [Number(row.id), row]));
  const saleByProductId = new Map(saleRows.map((row) => [Number(row.productId), row]));
  const profileByProductId = new Map(profileRows.map((row) => [Number(row.productId), row]));
  const packageMetaByPackageId = new Map(packageMetaRows.map((row) => [Number(row.packageId), row]));
  const packagesByProductId = packageRows.reduce((acc, row) => {
    const key = Number(row.productId);
    const list = acc.get(key) || [];
    list.push(row);
    acc.set(key, list);
    return acc;
  }, new Map());

  const matchedVendorNames = new Set();
  const filteredProducts = productRows
    .filter((row) => {
      if (row.isDeleted) return false;
      if (isMembershipCategoryName(categoryMap.get(Number(row.categoryId)))) return false;
      if (typeof vendorNameMatcher !== "function") return true;
      const vendorName = vendorMap.get(Number(row.vendorId))?.name || "";
      const matches = vendorNameMatcher(vendorName, row);
      if (matches) {
        matchedVendorNames.add(vendorName);
      }
      return matches;
    })
    .slice()
    .sort((left, right) => {
      const leftCategory = String(categoryMap.get(Number(left.categoryId)) || "Uncategorized");
      const rightCategory = String(categoryMap.get(Number(right.categoryId)) || "Uncategorized");
      return (
        leftCategory.localeCompare(rightCategory) ||
        String(left.name || "").localeCompare(String(right.name || ""))
      );
    });

  const sheetValues = [ORDERED_COLUMN_NAMES];
  const highlightedRowIndices = [];

  for (const product of filteredProducts) {
    const productId = Number(product.id);
    const snapshot = computeProductPricingSnapshot({
      product,
      packages: packagesByProductId.get(productId) || [],
      packageMetaByPackageId,
      vendor: vendorMap.get(Number(product.vendorId)) || null,
      profile: profileByProductId.get(productId) || {
        productId,
        onSale: saleByProductId.get(productId)?.onSale ?? 0,
        saleDiscount: saleByProductId.get(productId)?.saleDiscount ?? 0
      }
    });

    const vendor = vendorMap.get(Number(product.vendorId)) || null;
    const usesSourcePricing = isSourcePricingVendor(vendor);
    const saleRow = saleByProductId.get(productId) || null;
    const chosenPackageRow =
      snapshot.packageRows
        .filter((row) => toNumber(row.basePrice) !== null)
        .sort((left, right) => Number(left.basePrice) - Number(right.basePrice))[0] || null;
    const rowNumber = sheetValues.length + 1;
    const retailSalesPriceCol = getColumnLetterByName("retailSalesPrice");
    const unitOfMeasureCol = getColumnLetterByName("dff_unit_of_measure");
    const lowestWeightCol = getColumnLetterByName("lowest_weight");
    const highestWeightCol = getColumnLetterByName("highest_weight");
    const sourceMultiplierCol = getColumnLetterByName("FFCSAFactor");
    const avgWeightUsedCol = getColumnLetterByName("avgWeightUsed");
    const packageQuantityUsedCol = getColumnLetterByName("packageQuantityUsed");
    const purchasePriceCol = getColumnLetterByName("ffcsaPurchasePrice");
    const memberMarkupCol = getColumnLetterByName("memberMarkup");
    const saleCol = getColumnLetterByName("sale");
    const saleDiscountCol = getColumnLetterByName("saleDiscount");
    const customerSaleShareFormula = `IF(${saleCol}${rowNumber},${saleDiscountCol}${rowNumber},0)`;
    const purchaseFormula = `=IF(LOWER(${unitOfMeasureCol}${rowNumber})="lbs",IF(OR(${retailSalesPriceCol}${rowNumber}="",${avgWeightUsedCol}${rowNumber}=""),"",ROUND(${retailSalesPriceCol}${rowNumber}*${avgWeightUsedCol}${rowNumber}*${sourceMultiplierCol}${rowNumber},2)),IF(OR(${retailSalesPriceCol}${rowNumber}="",${packageQuantityUsedCol}${rowNumber}=""),"",ROUND(${retailSalesPriceCol}${rowNumber}*${packageQuantityUsedCol}${rowNumber}*${sourceMultiplierCol}${rowNumber},2)))`;
    const memberFormulaBody = `IF(OR(${purchasePriceCol}${rowNumber}="",${memberMarkupCol}${rowNumber}=""),"",ROUND(${purchasePriceCol}${rowNumber}*(1+${memberMarkupCol}${rowNumber})*(1-${customerSaleShareFormula}),2))`;
    const memberFormula = `=${memberFormulaBody}`;
    const squareSalePriceFormulaBody = `IF(${retailSalesPriceCol}${rowNumber}="","",ROUND(${retailSalesPriceCol}${rowNumber}*(1-${customerSaleShareFormula}),2))`;
    const squareSalePriceFormula = `=IF(${saleCol}${rowNumber},${squareSalePriceFormulaBody},"")`;
    const isWeightBasedSourcePricing =
      usesSourcePricing && String(snapshot.profile.unitOfMeasure || "").toLowerCase() === "lbs";
    const isEachBasedSourcePricing =
      usesSourcePricing && String(snapshot.profile.unitOfMeasure || "").toLowerCase() !== "lbs";
    const saleEnabled = Boolean(snapshot.profile.onSale);
    const rowValues = {
      id: productId,
      localLineProductID: productId,
      category: categoryMap.get(Number(product.categoryId)) || "Uncategorized",
      vendor: vendor?.name || "",
      productName: product.name || "",
      packageName: buildPackageSummary(snapshot.packageRows, snapshot.profile.unitOfMeasure),
      retailSalesPrice: usesSourcePricing ? snapshot.profile.sourceUnitPrice : "",
      lowest_weight: isWeightBasedSourcePricing ? snapshot.profile.minWeight : "",
      highest_weight: isWeightBasedSourcePricing ? snapshot.profile.maxWeight : "",
      dff_unit_of_measure: usesSourcePricing ? snapshot.profile.unitOfMeasure : "",
      sale: saleEnabled ? "TRUE" : "",
      saleDiscount: saleEnabled ? snapshot.profile.saleDiscount : "",
      squareSalePrice: saleEnabled ? squareSalePriceFormula : "",
      packageCount: snapshot.packageRows.length,
      description: flattenSheetText(product.description),
      FFCSAFactor: usesSourcePricing ? snapshot.profile.sourceMultiplier : "",
      avgWeightUsed:
        isWeightBasedSourcePricing
          ? `=IF(AND(${lowestWeightCol}${rowNumber}<>"",${highestWeightCol}${rowNumber}<>""),AVERAGE(${lowestWeightCol}${rowNumber},${highestWeightCol}${rowNumber}),IF(${lowestWeightCol}${rowNumber}<>"",${lowestWeightCol}${rowNumber},IF(${highestWeightCol}${rowNumber}<>"",${highestWeightCol}${rowNumber},"")))`
          : "",
      packageQuantityUsed:
        isEachBasedSourcePricing
          ? (chosenPackageRow ? Number(chosenPackageRow.quantity || 1) : 1)
          : "",
      ffcsaPurchasePrice: purchaseFormula,
      ffcsaMemberSalesPrice: memberFormula,
      memberMarkup: snapshot.profile.memberMarkup,
      track_inventory: toBooleanLabel(Boolean(product.trackInventory)),
      visible: toBooleanLabel(Boolean(product.visible)),
      remoteSyncStatus: snapshot.profile.remoteSyncStatus || "",
      remoteSyncedAt: formatDateTime(snapshot.profile.remoteSyncedAt)
    };

    if (hasRecentPriceOrSaleChange(profileByProductId.get(productId) || null, saleRow)) {
      highlightedRowIndices.push(sheetValues.length);
    }

    sheetValues.push(
      ORDERED_COLUMN_NAMES.map((column) => toSheetValue(rowValues[column]))
    );
  }

  Object.defineProperty(sheetValues, "highlightedRowIndices", {
    value: highlightedRowIndices,
    enumerable: false,
    configurable: false,
    writable: false
  });

  return {
    sheetValues,
    rowCount: Math.max(sheetValues.length - 1, 0),
    vendorNames: [...matchedVendorNames].sort((left, right) => left.localeCompare(right)),
    highlightedRowCount: highlightedRowIndices.length
  };
}

export async function exportMasterPricelist({
  nodeEnv = process.env.NODE_ENV || "production",
  dryRun = false,
  skipGoogle = false,
  vendorNameMatcher = null,
  spreadsheetId = process.env.GOOGLE_SHEETS_SPREADSHEET_ID || "",
  tabName = process.env.GOOGLE_SHEETS_TAB_NAME || "prices"
} = {}) {
  const spreadsheetSummary = spreadsheetId
    ? `${spreadsheetId} (tab: ${tabName})`
    : "Not configured";

  const { sheetValues, rowCount, vendorNames, highlightedRowCount } = await buildSheetValues({ vendorNameMatcher });
  const vendorSummary = vendorNames.length ? vendorNames.join(", ") : "All vendors";
  const introductionValues = buildIntroductionValues({
    syncTimeUtc: new Date().toISOString(),
    environment: nodeEnv,
    spreadsheetSummary,
    vendorSummary
  });
  Object.defineProperty(introductionValues, "noteRowIndex", {
    value: INTRO_NOTE_ROW_INDEX,
    enumerable: false,
    configurable: false,
    writable: false
  });

  if (dryRun || skipGoogle) {
    return {
      mode: dryRun ? "dry-run" : "skip-google",
      nodeEnv,
      spreadsheetSummary,
      vendorNames,
      rowCount,
      highlightedRowCount,
      sampleRows: sheetValues.slice(0, 6)
    };
  }

  await syncPricelistToGoogleSheet(sheetValues, introductionValues);
  return {
    mode: "google-sync",
    nodeEnv,
    spreadsheetSummary,
    vendorNames,
    rowCount,
    highlightedRowCount
  };
}

async function main() {
  const envFile = resolveFromRepoRoot(getArg("env-file") || ".env");
  const nodeEnv =
    getArg("node-env") ||
    (path.basename(envFile).match(/^\.env\.(.+)$/) || [])[1] ||
    process.env.NODE_ENV ||
    "production";
  const dryRun = hasFlag("dry-run");
  const skipGoogle = hasFlag("skip-google");

  dotenv.config({ path: envFile });

  const summary = await exportMasterPricelist({
    nodeEnv,
    dryRun,
    skipGoogle
  });

  if (dryRun || skipGoogle) {
    console.log(
      JSON.stringify(
        {
          ...summary,
          envFile
        },
        null,
        2
      )
    );
    return;
  }

  console.log(`Exported ${summary.rowCount} rows to Google Sheets.`);
}

const isDirectExecution =
  process.argv[1] && path.resolve(process.argv[1]) === __filename;

if (isDirectExecution) {
  main().catch((error) => {
    console.error(error?.message || error);
    process.exit(1);
  });
}
