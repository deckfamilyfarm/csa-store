function resolveFromRepoRoot(targetPath) {
  if (!targetPath) return targetPath;
  return path.isAbsolute(targetPath)
    ? targetPath
    : path.resolve(repoRoot, targetPath);
}

function base64UrlEncode(input) {
  const buffer = Buffer.isBuffer(input) ? input : Buffer.from(input);
  return buffer
    .toString("base64")
    .replace(/=/g, "")
    .replace(/\+/g, "-")
    .replace(/\//g, "_");
}

function columnIndexToLetter(index) {
  let column = "";
  let remainder = index;
  while (remainder > 0) {
    const letterIndex = (remainder - 1) % 26;
    column = String.fromCharCode(65 + letterIndex) + column;
    remainder = Math.floor((remainder - 1) / 26);
  }
  return column;
}

function getColumnLetterByName(columnName) {
  const index = COLUMN_INDEX_BY_NAME.get(columnName);
  if (!Number.isFinite(index)) {
    throw new Error(`Unknown Google pricelist column: ${columnName}`);
  }
  return columnIndexToLetter(index + 1);
}

